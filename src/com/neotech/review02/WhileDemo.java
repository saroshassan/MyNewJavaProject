package com.neotech.review02;

public class WhileDemo {

	public static void main(String[] args) {
		String greeting="Miremengjes";
		
		int count=1;
		
		while(count<=5)
		{
			System.out.println(count+" "+greeting);
			count++;
			
		}
		
		System.out.println("-----------");
		
		int number=0;
		while(number<5) {
			
			number++;
			
		System.out.println(number+" "+greeting);
		}
		
		
	}
			
		
		

	}

